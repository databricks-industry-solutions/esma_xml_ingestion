# MAGIC %md
# MAGIC ## Write to Delta Raw Table
# MAGIC 
# MAGIC Stream the enriched XML data into a Delta Lake table with checkpointing for reliable ingestion. Delta Lake provides ACID transactions, time travel, and schema evolution capabilities that are essential for production data pipelines.
# MAGIC 
# MAGIC Checkpointing ensures exactly-once processing - if the job fails or is stopped, it resumes from the last successful batch without reprocessing or missing data. The `availableNow` trigger processes all available data then stops, making it ideal for scheduled jobs rather than continuously running streams. The notebook creates the catalog and schema if they don't exist, then writes the streaming DataFrame to Delta format with append mode.

