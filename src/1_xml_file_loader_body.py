# MAGIC %md
# MAGIC ## Extract Header and Metadata from XML Files
# MAGIC 
# MAGIC **What:** Define a UDF that extracts document headers and metadata while excluding payload data records
# MAGIC 
# MAGIC **Why:** Regulatory XML files contain both control information (sender, timestamps, file batching) and business data (transactions). Separating these enables efficient metadata queries without scanning large payload tables. Header information is often needed for data lineage and audit trails.
# MAGIC 
# MAGIC **How:** The UDF uses streaming XML parsing (lxml iterparse) to read the file until it reaches the first row tag element, capturing only the header portion. This avoids loading entire large XML files into memory.

# COMMAND ----------

from lxml import etree
from pyspark.sql.functions import udf
from pyspark.sql.types import StringType

def strip_namespace(tag: str) -> str:
    """Remove XML namespace from tag."""
    return tag.split('}')[-1] if '}' in tag else tag

def remove_empty_elements(element):
    children_to_remove = []
    for child in list(element):
        if remove_empty_elements(child):
            children_to_remove.append(child)
    for child in children_to_remove:
        element.remove(child)
    has_children = len(list(element)) > 0
    has_attributes = bool(element.attrib)
    has_meaningful_text = (element.text and element.text.strip()) or (element.tail and element.tail.strip())
    is_completely_empty = not has_children and not has_attributes and not has_meaningful_text
    return is_completely_empty

def extract_hdr_pyld_metadata(file_path: str, row_tag: str = "Stat") -> str:
    try:
        context = etree.iterparse(file_path, events=("start", "end"), recover=True)
        element_stack = []
        skip_depth = 0
        root = None
        found_row_tag = False
        
        for event, elem in context:
            tag_name = strip_namespace(elem.tag)
            
            if event == "start":
                if tag_name == row_tag and not found_row_tag:
                    found_row_tag = True
                    elem.clear()
                    break
                should_skip = (skip_depth > 0) or (tag_name == row_tag)
                if should_skip:
                    skip_depth += 1
                else:
                    new_elem = etree.Element(elem.tag, attrib=elem.attrib)
                    new_elem.tag = strip_namespace(new_elem.tag)

                    if element_stack:
                        element_stack[-1].append(new_elem)
                    else:
                        root = new_elem
                    element_stack.append(new_elem)
            elif event == "end":
                if skip_depth > 0:
                    skip_depth -= 1
                else:
                    if element_stack:
                        current_elem = element_stack.pop()
                        current_elem.text = elem.text
                        current_elem.tail = elem.tail
                elem.clear()
        
        if root is not None:
            remove_empty_elements(root)
            return etree.tostring(root, encoding="unicode", pretty_print=True)
        return None
        
    except Exception as e:
        print(f"Error processing {file_path}: {str(e)}")
        return None

extract_hdr_pyld_metadata_udf = udf(extract_hdr_pyld_metadata, StringType())

