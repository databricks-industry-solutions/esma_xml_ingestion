# MAGIC %md
# MAGIC ## Extract File Naming Convention Metadata
# MAGIC 
# MAGIC Parse structured information from XML filenames using regex patterns. ESMA regulatory files follow naming conventions that encode batch information, version numbers, and submission dates. Extracting these fields enables you to track file processing completeness (have all files in a batch arrived?), identify resubmissions (version numbers), and organize data by submission date.
# MAGIC 
# MAGIC The regex patterns extract batch index, batch size, file version, and ESMA date from filenames. For example, a filename like `EMIR_123456_001-100-1_20231015.xml` indicates file 1 of 100 in batch 123456, version 1, submitted on October 15, 2023.

# COMMAND ----------

#TODO: Join hdr_pyld_metadata_df with the table that contains file_name and zip_name to run regex on that before the join

file_index_pattern = r"\d\d\d\d\d\d-\d"
esma_date_pattern = r"-\d\d\d\d\d\d_"

hdr_pyld_mtdt_rgx_df = (hdr_pyld_metadata_df
    .withColumn(
        "FileBatchIndex",
        substring(regexp_extract(col("file_name"), file_index_pattern, 0),1,3)
    )
    .withColumn(
        "FileBatchSize",
        substring(regexp_extract(col("file_name"), file_index_pattern, 0),4,3)
    )
    .withColumn(
        "FileVersion",
        substring(regexp_extract(col("file_name"), file_index_pattern, 0),8,1)
    )
    .withColumn(
        "ESMADate",
        concat(
            substring(regexp_extract(col("file_name"), esma_date_pattern, 0), 2, 2), lit('-'),
            substring(regexp_extract(col("file_name"), esma_date_pattern, 0), 4, 2), lit('-'),
            substring(regexp_extract(col("file_name"), esma_date_pattern, 0), 6, 2)
        )
    )
).drop("file_name")

