# MAGIC 
# MAGIC Load the JSON schemas created by the XSD processing notebook into Spark StructType objects. Schemas enable type-safe XML parsing and automatic column creation in Spark DataFrames, while avoiding the overhead of schema inference.
# MAGIC 
# MAGIC Using predefined schemas improves performance and ensures consistent data types across pipeline runs. The function reads each JSON schema file and converts it to a Spark StructType that the XML reader can use for parsing.

# COMMAND ----------

def readSchema(file: str) -> StructType:
    """
    Reads a JSON schema from a file and returns a Spark StructType object.

    Args:
        file (str): Path to the JSON schema file.

    Returns:
        StructType: Spark schema object parsed from the JSON file.
    """
    with open(file, 'r') as f:
        schema_json = f.read()
    schema = StructType.fromJson(json.loads(schema_json))
    return schema

