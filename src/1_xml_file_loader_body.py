# MAGIC %md
# MAGIC ## Write to Delta Raw Table
# MAGIC 
# MAGIC **What:** Stream the enriched XML data into a Delta Lake table with checkpointing
# MAGIC 
# MAGIC **Why:** Delta Lake provides ACID transactions, time travel, and schema evolution. Checkpointing ensures exactly-once processing - if the job fails, it resumes from the last successful batch. The `availableNow` trigger processes all available data then stops, ideal for scheduled jobs.
# MAGIC 
# MAGIC **How:** Create catalog and schema if needed, then write the streaming DataFrame to Delta format with append mode.

